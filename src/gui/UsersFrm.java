    private void loadTableUsers(List<Users> readAll) {
        Vector colunm = new Vector();
        colunm.add("ID");
        colunm.add("USERSNAME");
        colunm.add("PASSWORD");
        colunm.add("ROLEID");
        Vector rows = new Vector();
        for (Users u : readAll) {
            Vector row = new Vector();
            row.add(u.getId());
            row.add(u.getUsersname());
            row.add(u.getPassword());
            row.add(u.getRoleid());
            rows.add(row);
        }
        tblMain.setModel(new DefaultTableModel(rows, colunm));
        tblMain.updateUI();
        spnMain.setViewportView(this.tblMain);
    }

    private void loadRoles(List<Roles> readAll) {
        Vector v = new Vector();
        readAll.forEach((r) -> {
            v.add(r);
        });
        txtRoleid.setModel(new DefaultComboBoxModel<>(v));

    }
